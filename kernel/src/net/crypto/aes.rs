const RCON: [u8; 10] = [0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1b,0x36];

// ── Helpers ─────────────────────────────────────────────────

#[inline(always)]
pub fn xtime(a: u8) -> u8 {
    if (a & 0x80) != 0 { (a << 1) ^ 0x1b } else { a << 1 }
}

#[inline]
pub fn gf_mul(mut a: u8, mut b: u8) -> u8 {
    let mut p = 0u8;
    for _ in 0..8 {
        if (b & 1) != 0 { p ^= a; }
        let hi = a & 0x80;
        a <<= 1;
        if hi != 0 { a ^= 0x1b; }
        b >>= 1;
    }
    p
}

// ── AES-128 key schedule ────────────────────────────────────

/// Expand 16-byte key → 11 round keys (AES-128).
pub fn aes128_key_schedule(key: &[u8; 16]) -> [[u8; 16]; 11] {
    let mut rk = [[0u8; 16]; 11];
    rk[0].copy_from_slice(key);
    for i in 1..11 {
        let prev = rk[i - 1];
        let t = [
            SBOX[prev[13] as usize] ^ RCON[i - 1],
            SBOX[prev[14] as usize],
            SBOX[prev[15] as usize],
            SBOX[prev[12] as usize],
        ];
        for j in 0..4 { rk[i][j] = prev[j] ^ t[j]; }
        for j in 4..16 { rk[i][j] = prev[j] ^ rk[i][j - 4]; }
    }
    rk
}

// ── AES-256 key schedule ────────────────────────────────────

/// Expand 32-byte key → 15 round keys (AES-256).
pub fn aes256_key_schedule(key: &[u8; 32]) -> [[u8; 16]; 15] {
    let mut rk = [[0u8; 16]; 15];
    rk[0].copy_from_slice(&key[..16]);
    rk[1].copy_from_slice(&key[16..32]);

    let mut rcon_idx = 0;
    for i in 2..15 {
        let prev = rk[i - 1];
        let pprev = rk[i - 2];

        if (i & 1) == 0 {
            // Even: RotWord + SubWord + Rcon
            let t = [
                SBOX[prev[13] as usize] ^ RCON[rcon_idx],
                SBOX[prev[14] as usize],
                SBOX[prev[15] as usize],
                SBOX[prev[12] as usize],
            ];
            rcon_idx += 1;
            for j in 0..4  { rk[i][j] = pprev[j] ^ t[j]; }
            for j in 4..16 { rk[i][j] = pprev[j] ^ rk[i][j - 4]; }
        } else {
            // Odd: SubWord only
            let t = [
                SBOX[prev[0] as usize],
                SBOX[prev[1] as usize],
                SBOX[prev[2] as usize],
                SBOX[prev[3] as usize],
            ];
            for j in 0..4  { rk[i][j] = pprev[j] ^ t[j]; }
            for j in 4..16 { rk[i][j] = pprev[j] ^ rk[i][j - 4]; }
        }
    }
    rk
}

// ── AES encrypt/decrypt block ───────────────────────────────

/// AES block encrypt (works for both AES-128 and AES-256).
/// `nr` = number of rounds (10 for AES-128, 14 for AES-256).
pub fn aes_encrypt_block(block: &mut [u8; 16], rk: &[[u8; 16]], nr: usize) {
    // Initial AddRoundKey
    for i in 0..16 { block[i] ^= rk[0][i]; }

    for round in 1..nr {
        sub_bytes(block);
        shift_rows(block);
        mix_columns(block);
        for i in 0..16 { block[i] ^= rk[round][i]; }
    }
    // Final round (no MixColumns)
    sub_bytes(block);
    shift_rows(block);
    for i in 0..16 { block[i] ^= rk[nr][i]; }
}

/// AES block decrypt.
pub fn aes_decrypt_block(block: &mut [u8; 16], rk: &[[u8; 16]], nr: usize) {
    for i in 0..16 { block[i] ^= rk[nr][i]; }

    for round in (1..nr).rev() {
        inv_shift_rows(block);
        inv_sub_bytes(block);
        for i in 0..16 { block[i] ^= rk[round][i]; }
        inv_mix_columns(block);
    }
    inv_shift_rows(block);
    inv_sub_bytes(block);
    for i in 0..16 { block[i] ^= rk[0][i]; }
}

// convenience wrappers

pub fn aes128_encrypt(block: &mut [u8; 16], rk: &[[u8; 16]; 11]) {
    aes_encrypt_block(block, rk, 10);
}
pub fn aes128_decrypt(block: &mut [u8; 16], rk: &[[u8; 16]; 11]) {
    aes_decrypt_block(block, rk, 10);
}
pub fn aes256_encrypt(block: &mut [u8; 16], rk: &[[u8; 16]; 15]) {
    aes_encrypt_block(block, rk, 14);
}
pub fn aes256_decrypt(block: &mut [u8; 16], rk: &[[u8; 16]; 15]) {
    aes_decrypt_block(block, rk, 14);
}

// ── AES-CBC (kept for TLS 1.2 compat) ──────────────────────

use alloc::vec::Vec;

/// AES-128-CBC encrypt with PKCS#7 padding.
pub fn aes128_cbc_encrypt(key: &[u8; 16], iv: &[u8; 16], plaintext: &[u8]) -> Vec<u8> {
    let rk = aes128_key_schedule(key);
    let pad = 16 - (plaintext.len() % 16);
    let mut data = Vec::from(plaintext);
    for _ in 0..pad { data.push(pad as u8); }

    let mut out = Vec::with_capacity(data.len());
    let mut prev = *iv;
    for chunk in data.chunks(16) {
        let mut block = [0u8; 16];
        for i in 0..16 { block[i] = chunk[i] ^ prev[i]; }
        aes128_encrypt(&mut block, &rk);
        prev = block;
        out.extend_from_slice(&block);
    }
    out
}

/// AES-128-CBC decrypt with PKCS#7 unpadding.
pub fn aes128_cbc_decrypt(key: &[u8; 16], iv: &[u8; 16], ct: &[u8]) -> Option<Vec<u8>> {
    if ct.len() % 16 != 0 || ct.is_empty() { return None; }
    let rk = aes128_key_schedule(key);
    let mut out = Vec::with_capacity(ct.len());
    let mut prev = *iv;
    for chunk in ct.chunks(16) {
        let mut block = [0u8; 16];
        block.copy_from_slice(chunk);
        aes128_decrypt(&mut block, &rk);
        for i in 0..16 { block[i] ^= prev[i]; }
        prev.copy_from_slice(chunk);
        out.extend_from_slice(&block);
    }
    let pad = *out.last()? as usize;
    if pad == 0 || pad > 16 { return None; }
    out.truncate(out.len() - pad);
    Some(out)
}

// ── Internal round operations ───────────────────────────────

fn sub_bytes(b: &mut [u8; 16]) {
    for i in 0..16 { b[i] = SBOX[b[i] as usize]; }
}
fn inv_sub_bytes(b: &mut [u8; 16]) {
    for i in 0..16 { b[i] = INV_SBOX[b[i] as usize]; }
}

fn shift_rows(b: &mut [u8; 16]) {
    let t = *b;
    //row 1: shift left 1
    b[1]=t[5]; b[5]=t[9]; b[9]=t[13]; b[13]=t[1];
    //row 2: shift left 2
    b[2]=t[10]; b[6]=t[14]; b[10]=t[2]; b[14]=t[6];
    //row 3: shift left 3
    b[3]=t[15]; b[7]=t[3]; b[11]=t[7]; b[15]=t[11];
}
fn inv_shift_rows(b: &mut [u8; 16]) {
    let t = *b;
    b[1]=t[13]; b[5]=t[1]; b[9]=t[5]; b[13]=t[9];
    b[2]=t[10]; b[6]=t[14]; b[10]=t[2]; b[14]=t[6];
    b[3]=t[7]; b[7]=t[11]; b[11]=t[15]; b[15]=t[3];
}

fn mix_columns(b: &mut [u8; 16]) {
    for c in 0..4 {
        let i = c * 4;
        let (a0,a1,a2,a3) = (b[i],b[i+1],b[i+2],b[i+3]);
        b[i]   = xtime(a0) ^ xtime(a1) ^ a1 ^ a2 ^ a3;
        b[i+1] = a0 ^ xtime(a1) ^ xtime(a2) ^ a2 ^ a3;
        b[i+2] = a0 ^ a1 ^ xtime(a2) ^ xtime(a3) ^ a3;
        b[i+3] = xtime(a0) ^ a0 ^ a1 ^ a2 ^ xtime(a3);
    }
}
fn inv_mix_columns(b: &mut [u8; 16]) {
    for c in 0..4 {
        let i = c * 4;
        let (a0,a1,a2,a3) = (b[i],b[i+1],b[i+2],b[i+3]);
        b[i]   = gf_mul(a0,14) ^ gf_mul(a1,11) ^ gf_mul(a2,13) ^ gf_mul(a3,9);
        b[i+1] = gf_mul(a0,9)  ^ gf_mul(a1,14) ^ gf_mul(a2,11) ^ gf_mul(a3,13);
        b[i+2] = gf_mul(a0,13) ^ gf_mul(a1,9)  ^ gf_mul(a2,14) ^ gf_mul(a3,11);
        b[i+3] = gf_mul(a0,11) ^ gf_mul(a1,13) ^ gf_mul(a2,9)  ^ gf_mul(a3,14);
    }
}
